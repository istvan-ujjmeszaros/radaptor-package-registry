	/**
	 * Auto-detect contact person ID from value
	 * Value wrapped in underscores (_name_) searches by name
	 * Plain value is treated as ID.
	 */
	public static function autoDetectId(string $value): int
	{
		$value = trim($value);

		if (trim($value, '_') == '') {
			return self::ERROR_CONTACTPERSON_ID_EMPTY;
		}

		if (trim($value, '_') !== $value) {
			// find by name
			$contactpersons = DbHelper::selectMany('contactpersons', ['name' => trim($value, '_')], false, '', 'id');
		} else {
			// find by id (to check existence)
			$contactpersons = DbHelper::selectMany('contactpersons', ['id' => trim($value)], false, '', 'id');
		}

		if (count($contactpersons) == 0) {
			return self::ERROR_CONTACTPERSON_NOT_FOUND;
		}

		if (count($contactpersons) > 1) {
			return self::ERROR_MULTIPLE_CONTACTPERSONS;
		}

		return $contactpersons[0]['id'];
	}

	/**
	 * Find contact person ID by name and company ID.
	 */
	public static function getIdFromNameAndCompanyId(string $name, int $connected_company_id): ?int
	{
		$return = DbHelper::selectOne(
			table: 'contactpersons',
			where: [
				'name' => $name,
				'connected_company_id' => $connected_company_id,
			],
			cols: 'id'
		);

		return $return['id'] ?? null;
	}
