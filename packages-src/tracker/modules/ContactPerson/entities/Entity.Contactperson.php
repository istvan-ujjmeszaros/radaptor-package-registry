	/**
	 * Get contact persons for autocomplete select.
	 * @return array<int, ShapeEntityContactpersonSelectRow>
	 */
	public static function getListForSelect(string $term): array
	{
		$query = "SELECT cp.name, cp.id, c.shortname FROM contactpersons cp LEFT JOIN companies c ON c.id=cp.connected_company_id WHERE cp.name LIKE :term";
		$stmt = Db::instance()->prepare($query);
		$stmt->execute(['term' => "%{$term}%"]);

		$rs = $stmt->fetchAll(PDO::FETCH_ASSOC);

		$return = [];

		foreach ($rs as $value) {
			$return[] = [
				'inputtype' => 'option',
				'value' => $value['id'],
				'label' => $value['shortname'] == '' ? "{$value['name']}" : "{$value['name']} ({$value['shortname']})",
			];
		}

		return $return;
	}

